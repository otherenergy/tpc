					<p>Esta opción es muy fácil de llevar a cabo pero es la que más tiempo va a tardar. Consiste en hacer líneas de diferentes colores que se crucen entre sí. <strong>Haces las de un color, esperas a que se sequen y así sucesivamente</strong>. Te recomendamos utilizar 3 colores y, sobre todo, empezar por los tonos más claros primero.</p>

