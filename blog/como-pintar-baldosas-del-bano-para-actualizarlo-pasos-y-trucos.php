					<p>Hemos encontrado un campo en el que no se requiere experiencia para convertirte en todo un especialista: pintar las baldosas del baño. <strong>Gracias a esta revolucionaria forma de decoración, podrás darle otro aire a tu lavabo, a tu gusto, en tiempo récord y sin obras</strong>. </p>
					<p>Si no te quieres gastar apenas dinero, toma nota de estos pasos. Si los sigues vas a tener un baño nuevo antes de lo que crees.</p>

					<h3 class="verde mt-5">1 - Limpiar y preparar el soporte</h3>
					<p>Antes del 2 va el 1. Este paso sería el 1. Por lo tanto, iría antes de ponerse a pintar y, además, es el que te va a permitir, una vez hayas elegido el color con tu gusto exquisito, que el domingo de reforma (sin obras) sea todo un éxito.</p>

					<p>Así que ya sabes, coge un trapo húmedo (con agua y jabón suele funcionar) y pásalo por las baldosas. Con una vez suele ser suficiente para quitar el polvo o algo de grasa. </p>

					<p>Si la cosa se pone difícil, con nuestro limpiador Smart Cleaner quedarán como espejos.</p>

					<p>Y, antes de dejar este punto; las juntas. Si te queda alguna por recubrir, utiliza un tapajuntas. El nuestro, Smart Jointer de 0 a 2 mm, funciona de categoría.</p>

					<h3 class="verde mt-5">2 - Delimita la zona</h3>
					<p>Ahora, coge cinta aislante y marca la zona sobre la que vas a pintar. Esto te ayuda a visualizar claramente la zona de actuación y, a la vez, proteger la que no va a ser pintada.</p>

					<p>Parece un paso que, en ciertos días, se podría saltar pero te recomendamos hacerlo de todas todas.</p>

					<h3 class="verde mt-5">3, 2, 1... ¡A pintar!</h3>
					<p>Calma, porque es muy sencillo, como llevamos prometiéndote durante todo el artículo. <strong>Solo tienes que verter el contenido de tu cubo, humedecer tu rodillo o brocha y aplicar dos capas de pintura sobre las baldosas</strong>. Listo. </p>

					<p>Una vez aplicada la primera capa, es hora de quitar la cinta adhesiva, con cuidado, y dejar secar entre 2 y 3 horas. Cuando hayan pasado, aplicar otra capa y 2-3 horas más de secado. Ya lo tienes.<strong>Fácil, sencillo y para toda la familia, ¿ves?</strong></p>

