					<p><b>Paso 2: Protección de superficies y elementos aledaños</b></p>
					<p>El microcemento es un material líquido que se aplica con una llana, por lo que es importante que protejas las superficies y elementos alrededor del área dónde vas a trabajar. Te recomendamos cubrir el suelo, las paredes, la bañera y cualquier otro elemento que no quieras manchar con papel protector o plástico.</p>

