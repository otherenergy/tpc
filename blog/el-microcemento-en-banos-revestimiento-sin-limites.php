					<h3> Guía básica para aplicar microcemento en baños:</h3>

					<p><b>Paso 1: Preparación de la superficie</b></p>
					<p>El primer paso para aplicar microcemento en un baño es preparar la superficie correctamente. Debe estar limpia, seca, libre de polvo, grasa, óxido, pintura y cualquier otro tipo de contaminación que pueda afectar la adherencia del microcemento. Es importante también eliminar cualquier desperfecto o irregularidad que la superficie pueda tener, como grietas , agujeros o desconchones.</p>

