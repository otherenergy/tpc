					<h3>Decoración con fibras naturales</h3>

					<p>Considerado como uno de los materiales más demandados para la decoración de interiores gracias a su gran versatilidad, que permite al decorador obtener resultados distinguidos y únicos. Su flexibilidad es la responsable de ofrecer un amplio abanico de productos y diferentes elementos decorativos con los cuales decorar el dormitorio. Un material resistente y de singular textura que genera espacios con gran personalidad en un estilo moderno y artesanal.</p>

					<p>Este producto lo puedes encontrar como alfombras, cortinas, sillas, cestas, cubrecamas, sillones, entre otras muchas más formas de uso. Desde luego, te sorprendería al ver la infinidad de usos que se le puede dar.</p>

					<h3 style="padding-top:3%">Decoración con cerámica</h3>

					<p>La cerámica es una de esas artes que nunca pasan de moda. Si alguna vez has tenido alguna pieza, adorno, baratija o utensilio, sabrás que seguramente te acompañará para toda la vida. Son productos que debido a su autenticidad les brinda un gran valor tanto decorativo como sentimental. Y sí, también hablamos de la clásica taza de arcilla que hacemos a nuestros padres cuando somos pequeños, aunque algunos no seamos tan buenos artesanos como nos gustaría.</p>

					<p>Su propiedad más especial es esa aura de autor que la hace única e irrepetible. Una pequeña obra de arte que combina con cualquier estilo y que servirá como elemento de contraste para generar ambientes cargados de energía positiva.</p>

					<p>El uso de la cerámica cada vez está más distendido y si hace unos años se apreciaba únicamente como un objeto decorativo, cada vez se le encuentra un mayor uso a modo funcional, como antaño.</p>

					<p>Algunos de los papeles que puede jugar la cerámica en tu dormitorio matrimonial es como decoración para pared, como recipiente en tu mesilla de noche o estantería para depositar los pendientes, anillos, collares, etc. También existe la posibilidad de contemplarlos directamente en el suelo, como jarrones u otros objetos que brinden una estética más tribal y atractiva a tu cuarto.</p>

