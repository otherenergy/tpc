					<h3>Decoración con vidrio reutilizado</h3>

					<p>¿Tienes un bote de miel? ¿Un botellín? O, por el contrario, ¿algún frasco al cual no le des uso? Pues antes de tirar nada de esto debes saber que cada vez son más los diseñadores que apuestan por la reutilización de estos elementos de cristal a modo de decoración. Solo debes dejarte llevar por tu imaginación y encontrarás como darles una nueva vida a modo de objeto de decoración.</p>

					<p>Ya sean tratados, pintados, o dejándolos tal cual como están, ya se han visto decorando los dormitorios matrimoniales en forma de lámparas de noche, a modo de recipiente o como elemento decorativo en las diferentes superficies.</p>

					<p>Podrás apreciar la gran versatilidad que nos ofrecen los productos naturales para la decoración. Dependiendo del tamaño, la forma y el material, estos productos pueden ser colocados en casi cualquier lugar que se te ocurra del dormitorio: cerámicas en la mesilla de noche o colgadas en la pared, un jarrón o una silla en el suelo, cubrecamas de fibra vegetal u otros materiales... Como ves, lo ecológico está de moda y sus posibilidades decorativas son la prueba.</p>

					<h2 style="padding-top: 3%">3. Los colores neutros y empolvados, son tendencia</h2>

					<p>Las últimas tendencias en decoración de dormitorios de matrimonio siguen apostando por los colores neutros. Una corriente que sigue siendo popular entre los diseñadores y con la cual conseguimos ambientes estables y de gran valor decorativo. Colores como los grises, blancos, tonos tierra o arenosos, son un clásico que no pasan de moda. El objetivo es el de crear una base en toda la habitación con un tono neutro que de pie a realizar decoraciones modernas y combinables con una gran variedad de elementos.</p>

					<p>Se buscan colores tranquilos y relajantes, que llenen nuestros dormitorios con optimismo y buena energía. Gana gran valor la presencia del tacto, dando lugar a algunas decoraciones en las que encontraremos superficies con pinturas con cierta textura.</p>

