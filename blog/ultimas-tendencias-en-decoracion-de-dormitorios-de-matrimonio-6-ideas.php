					<p>Haciendo uso de tablones de madera, plantas y otros elementos decorativos, logramos un bello y pacífico ambiente rústico en el cual sentir una plena desconexión del ajetreo de las ciudades.</p>

					<h3>Estilo Nórdico</h3>

					<p>Y con el diseño nórdico podríamos decir que pasamos a la antítesis del estilo anterior. Una tendencia de naturaleza sutil y elegante, que defiende mantener los espacios aireados y luminosos y decorados con elementos con cierto brillo. En este, las superficies están pintadas en tonos suaves, pastel o neutros, destacando particularmente los blancos, grises, azules o rosas.</p>

					<p>Una de sus características más destacadas es el uso de muebles simples y con siluetas limpias y sencillas, dándole especial prioridad a lo artesanal.</p>

					<h3>Estilo Vintage</h3>

					<p>Un estilo que otorga a los dormitorios de una gran personalidad. Esta forma de decoración, casi anárquica, nos muestra dormitorios con apariencias únicas y singulares en las que rescatan objetos y estilos del pasado para dar a los espacios un aspecto innovador, valga la ironía.</p>

					<p>A diferencia del resto de estilos, en este cada dormitorio es totalmente diferente y exclusivo, ya que el componente personal es muy alto, propiedad que lo hace tan llamativo y con tanto carácter.</p>

					<p>Una tendencia decorativa en la que se ve recogida una gran variedad de estilos, pero  con la particularidad de tener que verse con un aspecto de décadas atrás. No es necesario que el mobiliario sea viejo originalmente, también está la opción de someter a los objetos a un proceso de envejecimiento. Con el estilo vintage llenarás tu dormitorio de pura creatividad y de una magia muy característica.</p>

