					<h2 style="padding-top: 3%">5. Pared trasera de la cama: componente estratégico</h2>

					<p>Uno de los elementos más relevantes y que mayor impacto tendrán sobre la imagen de nuestro dormitorio. La pared de detrás de la cama es una parte fundamental en la decoración de nuestra habitación matrimonial ya que es una de las superficies que más captan la atención de toda la estancia, de ahí la importancia de cuidar al detalle su apariencia. Debe ser una superficie con personalidad, que genere interés y que sea diferente al resto de paredes. La meta es que se convierta en un acento decorativo y para ello, os vamos a mostrar diferentes ideas con las que lograr alcanzar dicho objetivo.</p>

					<h3 style="padding-top: 3%" class="verde">Los cabeceros pisan fuerte</h3>
					<p>Los cabeceros están ganando fuerza año tras año. En esta temporada se han convertido en una de las apuestas más fuertes y entre ellos destacan los tapizados con colores lisos y tranquilos, como azul, verdes o beis, y también aquellos hechos con fibras naturales o con estampado botánico, representando una estética más alegre y divertida.</p>

					<p>La moda hace que desaparezcan los cabeceros de madera como uno de los favoritos, los cuales dominaron el mercado en los últimos años y dan paso a los respaldos mullidos, a fin de brindar al dormitorio de un aspecto más sofisticado y elegante. Como alternativa nacen los respaldos de dos o más colores, una propuesta con la cual generar un aporte decorativo muy interesante</p>

					<h3>Embriágate con las pinturas y murales decorativos</h3>

					<p>Probablemente una de las opciones que más alternativas decorativas nos puede aportar. Ya sea aplicando diferentes pinturas o mediante vinilos y papeles murales, la creatividad inundará los dormitorios para conseguir ambientes distinguidos.</p>
