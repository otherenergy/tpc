					<p>Los diseñadores de interior cada vez tienen más en cuenta los materiales ecológicos para sus planes de decoración. Esto no se debe únicamente a la perspectiva ética ante un problema global, no, si no por el gran potencial que representa a nivel decorativo. Con los productos artesanales las posibilidades para el diseño se multiplican vertiginosamente gracias a esa propiedad tan única que caracteriza a cada uno de estos elementos.</p>

