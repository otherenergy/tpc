					<h2 style="padding-top: 3%">1. Apuesta por ambientes despejados y limpios</h2>

					<p>Suficiente sobrecarga tenemos a diario con la gran cantidad de estímulos que recibimos a lo largo del día. Cuando llegamos a nuestro cuarto queremos despejar la mente y respirar esa tranquilidad tan característica del hogar, y para ello no hay mejor forma que evitando decorar nuestros dormitorios de forma muy recargada. Apuesta por ambientes despejados y sencillos, donde cada pieza decorativa contraste con la simplicidad de un fondo liso.</p>

					<p>Una de las tendencias más populares es el uso de accesorios o cuadros artísticos. Elementos con gran personalidad que resaltarán entre un espacio simple y que harán lucir a tu dormitorio con un carácter propio.</p>

