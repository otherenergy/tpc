					<h2 style="padding-top:0;">CONSEJOS PARA CREAR UNA OFICINA EN CASA</h2>
					<p>Delimitar el lugar de trabajo es importantísimo siempre, ya sea para trabajar desde casa de manera prolongada o para algo puntual.</p>
					<p>Una de las cosas más importantes es diferenciar los espacios. Trabajo y descanso no pueden estar ubicados en el mismo lugar. Y es que en ambas direcciones es contraproducente: ni se desconecta cuando hay que descansar, ni nos concentramos cuando hay que trabajar.</p>
					<p>Pero, ¿qué pasa si no contamos con espacio suficiente para destinar una habitación únicamente para nuestra <strong>oficina en casa</strong>? No pasa nada, hay más formas de delimitar espacios aunque no haya paredes de por medio. Solo necesitas un <strong>separador de ambientes</strong>, ya sea visual o material.</p>
					<p><span class="verde">Cambia el estilo entre un sitio y otro</span></p>
					<p>Por ejemplo, si estudio y salón conviven juntos, puedes poner las paredes que delimitan la zona de descanso de los sofás de un color y la zona de trabajo de otro. Incluso también puedes cambiar los materiales de la pared porque ¡hay vida más allá de la pintura! Existen algunos materiales decorativos fáciles y rápidos de poner y que, además, puedes aplicar tú mismo.</p>
					<p>La diferencia de texturas, colores y estilos entre los elementos nos permite diferenciar espacios aunque estén en la misma estancia.</p>
					<p><span class="verde">Utiliza los muebles como separador de ambientes</span></p>
					<p>Puedes usar el típico biombo para <strong>separar ambientes sin obras</strong> o, incluso, poner cortinas. Lo importante es crear la sensación divisoria que te ayude a desconectar y concentrarte, según lo que necesites en cada caso.</p>
					<p><span class="verde">Apuesta por lo natural</span></p>
					<p>La luz es imprescindible para un ambiente saludable de trabajo. Pero, además, <strong>la luz natural es esencial</strong> porque reduce el estrés y el cansancio visual. Sin duda, un aspecto importantísimo que deber tener tu <strong>oficina en casa</strong>.</p>
					<p>Otro elemento que ayuda mucho a crear vitalidad y positividad es el uso de plantas y flores. Necesitamos vida a nuestro alrededor y qué mejor que hacerlo de manera natural.</p>
					<p><span class="verde">Delimita tus horarios</span></p>
					<p>Debes organizar el horario con pausas incluidas y no hacer, por norma general, más horas de las que te corresponden porque la falta de descanso y de desconexión no ayuda a tu rendimiento.</p>
					<p><span class="verde">Elige bien los colores</span></p>
					<p>Si la luz es importante, los colores son imprescindibles porque crean sensaciones en nosotros. Por eso, en muchas ocasiones, pueden ayudarte en tu productividad.</br>Así, si tienes una profesión creativa, te beneficiará tener paredes de colores que enfaticen tu creatividad. Como, por ejemplo, el color amarillo, el azul o el naranja.</p>
					<p>Si necesitas muchísima concentración y relajación, los tonos beige, pastel y blancos serán tus mejores compañeros.</p>
