					<h2 class="verde pt-4">¿Qué ventajas tiene el cemento pulido?</h2>

					<ul>
						<li>Elaborado en obra con cemento, arena y agua, <strong>se produce de manera totalmente artesanal</strong>.</li>
						<li><strong>Aplicación manual</strong> con llana. Su mezcla se realiza en el lugar de la aplicación. Luego se extiende sobre el mallazo de hormigón forjado.</li>
						<li><strong>Gran adaptación estética</strong> a cualquier tipo de ambiente. Con su base neutra, comparte protagonismo con el estilo decorativo establecido desde un segundo plano muy discreto.</li>
						<li>Su <strong>uniformidad</strong> eleva la sensación de amplitud en cualquier espacio.</li>
						<li>Su <strong>grado de impermeabilización es bastante alto</strong>.</li>
