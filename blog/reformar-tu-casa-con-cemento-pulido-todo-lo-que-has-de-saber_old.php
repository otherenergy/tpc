					<h2 class="verde pt-4">Cemento pulido: ¿de qué se trata?</h2>
					<p>Este material se trata de una <strong>capa de cemento refinado</strong> a la que se le añade el colorante deseado y que ha de ser aplicada, pulida, con una máquina especial. Así, en sus <strong>5-10 cm de grosor</strong> encontramos una <strong>mezcla de áridos, aditivos, agua y pigmentos</strong>.</p>

					<p>Pensado tanto para exteriores como para interiores, es común encontrarlo en <strong>parkings, naves, fábricas, comercios, oficinas y viviendas particulares</strong> que necesitan pavimentos  de un espesor mayor preparados para resistir al tránsito de personas y vehículos, desde coches hasta camiones.</p>

					<h2 class="verde pt-4">Cemento pulido: un material exclusivo para suelos</h2>

					<p>El cemento pulido es un material que cuenta con una <strong>gran aceptación por todo tipo de públicos</strong>: profesionales de la aplicación, profesionales de la decoración y los usuarios que lo van a disfrutar en los suelos de sus viviendas o sus locales comerciales.</p>

					<p>Estamos ante un material que supo enamorar allá donde fue. Primero, <strong>su uso se extendió por el ámbito industrial o comercial</strong>, para luego ser acogido con los brazos abiertos en el ámbito doméstico. </p>

					<p>Por eso, no es de extrañar que estés pensando en darle un aire totalmente nuevo a tu vivienda cambiando tu suelo por uno más gris y más robusto, para crear <strong>estancias más modernas con acabados uniformes</strong>, que aportan confort y estilo a partes iguales y que, sobre todo, te van a convertir en la envidia de todos los vecinos.</p>

