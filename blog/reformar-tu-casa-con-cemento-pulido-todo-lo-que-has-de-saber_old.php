					<p>Esto se debe a <strong>su resistencia al tránsito, al peso y a la compresión</strong> que le llevan a convertirse en un aliado de primera para este tipo de espacios que están acostumbrados a ser visitados con frecuencia por personas pero, sobre todo, por <strong>coches y furgonetas con pesos muy superiores a la tonelada</strong>.</p>

