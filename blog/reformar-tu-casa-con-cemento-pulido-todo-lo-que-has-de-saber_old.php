					<h3 class="verde mt-4">Baños</h3>

					<p>El cemento pulido es <strong>ideal para utilizar en baños</strong>, tanto por sus capacidades decorativas como las funcionales. El estilo minimalista es una garantía en este material, de color uniforme pero con contrastes, que mezcla los acabados suaves con el orden y la sutileza.</p>

					<p>Si tenías pensado renovar tu baño con este material has de saber que aporta una impermeabilidad más que elevada, que le permite dar muy buenos rendimientos en ambientes húmedos. Ya sea para lavabo, ducha o bañeras, se comportará de maravilla. Eso sí, es necesaria aplicarle capas protectoras para que las superficies sobre las que se extiende no absorban el agua.</p>

					<h3 class="verde mt-4">Cocinas</h3>

					<p>Si por el contrario quieres reformar tu cocina, has de saber que los suelos de cemento pulido son pavimentos que combinan con materiales que se suelen utilizar en cocinas como lo son el cristal, el metal o la piedra. Gracias a su acabado brillante se adapta a cualquier color que comparta espacio con él. No tiene porque ser siempre de color gris, ya que se pueden obtener superficies de cemento pulido de colores como el rojo o el negro, si se pretende crear un ambiente en el que se respire más elegancia.</p>

