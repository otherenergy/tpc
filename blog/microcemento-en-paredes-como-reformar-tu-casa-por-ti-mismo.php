					<h3>Cómo hacer microcemento en paredes sobre materiales no absorbentes</h3>
					<p>En <strong>superficies no absorbentes</strong> como es el caso de los <strong>azulejos, el terrazo o las baldosas</strong>, la colocación de microcemento es diferente pero no te asustes porque el proceso sigue siendo igual de fácil y rápido como te explicamos a continuación.</p>

					<p>En primer lugar, has de <strong>limpiar la superficie de polvo y grasa</strong> para que el revestimiento se adhiera correctamente. Delimita las zonas que quieres recubrir y aplica una capa de Smart Jointer. Tras dejarlo secar durante 24 horas, lija la superficie con una lija grano 40. Una vez hecho esto, es hora de aplicar una capa de Smart Primer Grip y dejarla secar de 2 a 4 horas. Ahora es el momento de darle vida a tus paredes, aplica dos finas capas de Smart Base con una llana de acero dejando un margen de 6 horas entre capa y capa. No te olvides de lijarla posteriormente con una lija grano 40. </p>

					<p>Aquí llegan los dos últimos pasos: aplica dos finas capas de Smart Liso con una llana de goma y deja 6 horas de secado entre capa y capa, una vez aplicadas, deja 24 horas de espera para aplicar tres capas de Smart Varnish con un rodillo para <strong>proteger el revestimiento. Entre capa y capa deja un margen de entre 2 y 4 horas para su secado.</p>

