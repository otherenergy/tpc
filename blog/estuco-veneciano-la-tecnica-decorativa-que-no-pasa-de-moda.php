					<p>1. Preparación de la superficie. Antes de comenzar a aplicar el estuco veneciano, debes asegurarte de que la superficie esté limpia, seca y lisa. Si la pared o la superficie presenta irregularidades o imperfecciones, deberás repararlas antes de comenzar a aplicar el estuco. Para ello, puedes utilizar una masilla reparadora.</p>

					<p>2. Aplicación de la imprimación. Una vez que la superficie está limpia y preparada, deberás aplicar una imprimación que te ayudará a que el estuco se adhiera correctamente a la superficie. Puedes utilizar una imprimación específica para estuco veneciano o una imprimación universal.</p>

					<p>3. Preparación del estuco. A continuación, debes preparar el estuco veneciano. Para ello, deberás seguir las instrucciones del fabricante y mezclar el estuco con agua hasta conseguir una textura homogénea. Te recomendamos que prepares la cantidad necesaria para cubrir la superficie completa en una sola aplicación.</p>

					<p>4. Aplicación del estuco. Comienza aplicando el estuco veneciano en la superficie con una llana de acero inoxidable, extendiéndolo de manera uniforme. A continuación, realiza un segundo pase, esta vez con una llana de goma, para conseguir un efecto más suave y uniforme.</p>

