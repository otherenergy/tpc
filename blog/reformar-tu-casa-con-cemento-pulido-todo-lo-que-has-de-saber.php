					<p>Eso sí, aunque el cemento pulido es resistente, hay que tener cuidado con él. No se recomienda utilizar productos químicos agresivos para limpiarlo, y hay que protegerlo de golpes y arañazos para que no se rompa. Pero si se trata con cuidado, el cemento pulido puede durar muchos años y mantenerse en perfectas condiciones.</p>

