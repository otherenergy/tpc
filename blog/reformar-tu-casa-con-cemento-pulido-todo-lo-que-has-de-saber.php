					<h2 class="verde pt-4">Cemento pulido: ¿qué es?</h2>

					<p>¡Hablemos de cemento pulido! Seguro que habéis visto en más de una ocasión este tipo de acabado en suelos y paredes de locales y casas, pero quizás no sepáis muy bien qué es y cómo se hace. Pues bien, el cemento pulido es un material que se consigue a través del pulido mecánico de una capa de concreto con herramientmaleabilidad de diamante, dejando al descubierto el agregado y la pasta de cemento debajo. El resultado final es una superficie lisa, brillante y uniforme, que puede ser de diferentes colores dependiendo de los pigmentos utilizados.</p>

					<p>El cemento pulido se utiliza desde hace tiempo en naves industriales y lugares de alto tráfico, pero ahora se ha puesto de moda en la decoración de interiores y exteriores de casas y locales comerciales. ¿Por qué? Pues porque tiene un montón de ventajas: es resistente al desgaste, a la abrasión y a la compresión, aguanta las manchas y es muy fácil de limpiar. Además, su apariencia moderna y minimalista le da un toque de elegancia a cualquier espacio.</p>

