					<p>Se podría decir que la piedra es uno de los revestimientos con más encanto. Allá donde se coloque hará que se respire un aura especial, en un efecto rústico a la par que moderno. Este material destaca tanto por su estética como por sus cualidades en términos de resistencia.</p>

					<p>Si como yo, eres de los que se asa como un pollo con las olas de calor en verano, has de saber que con la piedra todo eso se ha acabado, ya que se trata de uno de los revestimientos que mejor aislamiento térmico te va a ofrecer, tanto para el calor como para el frío.</p>

					<p>Al tratarse de un producto cero poroso, la humedad tampoco es un problema, así como la proliferación de bacterias. Desde luego, con la piedra, la durabilidad a largo plazo está garantizada.</p>

