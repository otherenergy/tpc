					<h2 style="padding-top: 3%">¿De qué trata el revestimiento de pared?</h2>
					<p>De manera sencilla, el revestimiento para pared es aquella práctica en la cual se recubre la pared con un material a fin de darle una nueva apariencia, protegerla o ambas. Según el producto con el cual se está revistiendo, se podrá aplicar en más o menos superficies. Un ejemplo es el microcemento, un tipo de revestimiento decorativo capaz de adaptarse sobre cualquier soporte o entorno: interior, exterior, cocina, baño, suelo, pared, techo, escaleras, muebles...</p>

					<p>Una de las funciones más características de los revestimientos de pared es la propiedad de brindarle a la superficie una mayor resistencia, ya que la protege ante la humedad, los golpes, los rayos UV, las rayaduras y los cambios de temperatura.</p>

