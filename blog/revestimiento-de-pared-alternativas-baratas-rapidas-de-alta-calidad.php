					<p>El ladrillo comenzó a utilizarse en las viviendas neoyorquinas a modo de recorte de recursos, tratando de optimizar lo que ya se tenía a fin de ahorrar ante los excesivos precios de aquel entonces, hace alrededor de 40 años. </p>


