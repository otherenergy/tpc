					<h3 style="padding-top:3%">2. Utiliza la iluminación de forma creativa</h3>

					<p>La luz natural es muy importante en la decoración nórdica, pero también lo es la iluminación artificial. Es súúúper importante potenciar la entrada de la luz natural por las ventanas y, para ello y estrechamente relacionado con el punto anterior, se buscan muebles que no entorpezcan esta fuente de iluminación.</p>

					<p>De ahí la importancia de que el mobiliario esté formado por líneas sencillas y básicas y que no sea excesivamente alto, para no tapar la luz natural del exterior. Si eliges estores, estos han de ser muy simples y en colores neutros, para ayudar a que la iluminación sea siempre predominante. </p>

					<p>Por otra parte, la luz artificial de cada estancia ha de ser <strong>suave y cálida</strong>, pues esto ayudará a crear una atmósfera acogedora frente al minimalismo y sencillez que caracterizan de forma global el estilo nórdico.</p>

					<p>Existen algunas formas de jugar con estas fuentes de luz para destacar algunos de los rincones o zonas de tu estancia y que, así, decoren de una manera única y original. Por ejemplo, puedes elegir una lámpara o foco y dirigir su luz hacia un punto concreto de tu estancia, si quieres que éste tenga protagonismo o para crear un ambiente de lo más singular.</p>

					<p>En cualquier caso, asegúrate de que tu estancia cuente con luz suficiente que aporte calidez, cercanía y abrazo, pues este es uno de los elementos más importantes para crear lugares realmente acogedores y confortables.</p>

					<h3 style="padding-top:3%">3. La madera es el mejor aliado del estilo nórdico</h3>

					<p>Eso sí, asegúrate de que su obtención sea a través de explotación controlada o, al menos, sea <strong>ecológica</strong> o reciclada. Porque la madera predomina en muchos de los estilos de decoración y es especialmente popular en esta tendencia nórdica, pero también es predominante a día de hoy, el contar con materiales respetuosos con el medio ambiente.</p>

