					<p><strong>La lana, el algodón y las fibras naturales</strong> son excelentes para crear un ambiente acogedor y natural. Y esto es muy representativo del estilo nórdico, pero nos tiene también acostumbrados en cualquier tipo de decoración. ¿O a caso las cestas de mimbre no son un <must> en la mayoría de las viviendas?</must>

					<p>Decorar a través de los textiles y las texturas es una manera ideal de decorar sin recargar el ambiente. Y esto, recuerda, es fundamental en este tipo de decoración. Y, además de fundamental, resulta excesivamente fácil.</p>

					<p>Puedes incluir elementos de mimbre o rafia en los detalles pero también en alfombras o textiles. Siempre te quedará la lana y el algodón para los cojines y mantas de tu sofá o cama. Es muy fácil y efectivo y aporta ese toque de calidez que caracteriza la decoración nórdica para la creación de ambientes acogedores y completamente naturales.</p>

