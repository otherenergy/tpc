					<h2 style="padding-top: 2%">Qué es una malla para microcemento y para qué sirve</h2>

					<p>Empecemos por el principio. La malla para microcemento es un complemento fundamental para un proceso de instalación de garantías. Su función principal es la de evitar que, con el tiempo, aparezcan fisuras y grietas en el nuevo revestimiento. Un elemento especialmente importante en aquellas áreas que son propensas a movimientos y tensiones estructurales.</p>

					<p>Con la colocación de la malla sobre el microcemento, que tiene que estar fresco, se minimiza el riesgo de que el nuevo recubrimiento pueda cuartearse. Sobre todo en las superficies expuestas a vibraciones, cambios de temperatura y cargas pesadas.</p>

					<p>Pero más allá de ser un refuerzo para mantener a raya las fisuras superficiales, la malla mejora de manera notable la adherencia del microcemento como en el caso de las paredes. Asimismo contribuye a la durabilidad mismo.
					</p>

					<p>Uno de los materiales más usados en el sector de la construcción por cómo ayuda a estabilizar el nuevo soporte y reforzar paredes, suelos y techos. De igual forma, <strong>la resistencia al impacto se incrementa y resiste a la humedad</strong>.</p>

					<p>Pero, como ya hemos especificado en párrafos anteriores y volvemos a recalcar en éste,  el usar o no usar malla dependerá del modelo de microcemento y de sus necesidades características. Como ocurre con el microcemento listo al uso de <strong>Smartcret, formulado para ser aplicado sin malla sin comprometer su resistencia y durabilidad</strong>.</p>

					<h2 style="padding-top: 4%">Tipos de mallas para microcemento</h2>

					<p>Tip para no ser un pardill@ (siempre desde el cariño): el soporte tiene que estar nivelado, seco y libre de polvo. Si no lo está, ponte las pillas antes de colocar la malla.</p>

					<p>Volvemos al redil, que nos estamos yendo por los cerros de Úbeda. No hay un solo tipo de malla para microcemento. Principalmente hay dos modelos en el mercado: <strong>las mallas de fibra de vidrio</strong> y las <strong>mallas de fibra de poliéster</strong>.</p>

