					<p>Ambas son fáciles de manipular, flexibles y ligeras. Poseen la capacidad de absorber las dilataciones resultantes de los cambios de temperatura y no se oxidan ni se dañan al estar en contacto con el microcemento. A continuación, comentamos brevemente las diferencias más importantes entre ellas.</p>

					<h3> Mallas de fibra de vidrio para microcemento</h3>

					<p>Las mallas de fibra de vidrio son <strong>las más comunes</strong> y utilizadas en los trabajos de microcemento. Destacan por su <strong>elevada resistencia y durabilidad</strong>. Están compuestas por fibras de vidrio que han sido tejidas hasta formar una especie de red. Este tipo es genial para los suelos que sufren de tránsito elevado, incluido de vehículos, así como en zonas húmedas o expuestas a los agentes químicos.</p>

					<h3>Mallas de fibra de poliéster para microcemento</h3>

					<p><strong>Menos frecuentes pero más flexibles y sencillas de manipular</strong>. A diferencia de las de vidrio, las mallas de fibra de poliéster están formadas por fibras sintéticas que se entrelazan. Más económicas también pero, sin embargo, las tensiones estructurales de la superficie tienen que ser menores porque su <strong>resistencia es menor</strong>.</p>


