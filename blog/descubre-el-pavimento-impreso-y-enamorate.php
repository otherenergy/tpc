					<h3>2. Alisar y compactar el terreno.</h3>
					
					<p>Para ello necesitas un buen pisón o rodillo. En la base debes colocar un plástico para evitar que la deshidratación del suelo llegue al hormigón. Si en el proceso encuentras que necesitas quitar tierra, no hay problema, hazle espacio y cajéalo y cuando llegues al nivel deseado, compáctalo. El espesor promedio del hormigón impreso es de 10cm máximo para tránsito de peatones y hasta 15cm si es de tránsito vehicular.</p>
					
					<h3>3. Preparar el hormigón impreso.</h3>
					
					<p>Para su preparación debes mezclar una parte de cemento, tres partes de arena, cuatro partes de grava y 0,5 partes de agua. ¡Voilà, tú fórmula secreta está lista! Gracias a la variedad de moldes disponibles conseguirás que la superficie tenga diferentes formas y relieves.</p>
					
					<h3>4. Verter el hormigón y unificar la superficie.</h3>
					
					<p>Una vez que la mezcla este lista, es hora de verter el hormigón de manera uniforme  sobre el área, extenderla, reglearla y suavizar con la ayuda de una llana de magnesio. Añadiremos fibras de polipropileno al hormigón para mejorar sus resistencias.</p>
					
					<h3>5. Cortar las juntas de dilatación.</h3>
					
					<p>Una vez el hormigón impreso está completamente seco, corta las juntas de dilatación con la ayuda de disco de piedra y elimina el agente desmoldeante con una hidrolimpiadora. No solo le dará un aspecto increíble al suelo impreso, sino que también impedirá que se formen grietas cuando el hormigón se expanda y contraiga con los cambios de temperatura.</p>
					
					<h3>6. Imprimar el molde del pavimento impreso.</h3>
					
					<p>Antes de que el hormigón termine de fraguar completamente, utilizaremos moldes previamente seleccionados para imprimar el suelo de hormigón creando así patrones y texturas en la superficie. Como veremos más adelante hay una amplia variedad de diseños y formas, imitando piedra, ladrillo u otras texturas decorativas.</p>
					
					<h3> 7. Limpieza y sellado del pavimento impreso.</h3>
					
					<p>Aclarar con agua la superficie tratada para eliminar los restos del producto y dejar secar. Se puede utilizar una pistola de agua a una distancia prudencial de 50 centímetros y la boquilla en posición de abanico. No aplicar un chorro de agua directo sobre la superficie. </p>
				
