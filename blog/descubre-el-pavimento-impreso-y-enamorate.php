					<h2>Cómo hacer pavimento impreso: pasos para su aplicación</h2>
					
					<p>Esta técnica decorativa permite la impresión de diseños complejos en cualquier tipo de superficie como paredes, suelos, fachadas, escaleras o puentes. El pavimento impreso se adapta ya sea a casas acogedoras, grandes edificios industriales o locales comerciales, puesto que es un material muy versátil. Su instalación es rápida y sencilla. En un abrir y cerrar de ojos estará listo y funcionando.
					Si quieres mantener el pavimento de hormigón impreso como el primer día, debes seguir correctamente los siguientes pasos para su aplicación que te dejamos a continuación.</p>
					
					<h3>1. Limpieza de la superficie.</h3>
					
					<p>Antes de ponerte manos a la obra con esta aventura, tienes que asegurarte que la zona esté despejada de hojas y piedras, ya que esto influye directamente en la duración del pavimento impreso. Después debes preparar los niveles y pendientes del terreno, esto te permitirá darle forma esas curvas arriesgadas presentes en tú proyecto. </p>
					
				</div>
