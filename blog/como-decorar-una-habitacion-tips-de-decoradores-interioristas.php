					<h3>Mesitas de noche: un gran apoyo para la cama</h3>

					<p>Si algo no puede faltar en la decoración de una habitación de matrimonio son las dos mesitas de noche. Además de ir acorde al estilo general del dormitorio, han de ser prácticas y tener la capacidad suficiente como para que puedas guardar todo lo que necesitas tener a mano por la noche (e incluso por la mañana). </p>

					<p>Como todo elemento decorativo, hay mil estilos diferentes de mesitas de noche, las hay de madera, blancas, de ratán e incluso de cristal. Tan solo tienes que elegir la medida correcta y el diseño que más se ajuste a la decoración general de la habitación.</p>

					<p>Puedes colocar en ellas una lámpara y darle mayor protagonismo, o bien colocar un arreglo floral acompañando a una fotografía. Decidas apoyar en ellas lo que decidas, ten en cuenta que has de jugar con las formas y altura de los objetos para mejorar la estructura visual de la habitación. </p>

