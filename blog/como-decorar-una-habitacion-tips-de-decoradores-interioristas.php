					<p>Antes de ponernos manos a la obra con los tips y consejos, hay que tener claro una cosa: el elemento más importante de un dormitorio es <strong>la cama</strong>. Así que sí, <strong>la decoración de la habitación has de centrarla entorno a ella</strong>. </p>

					<p>El primer paso será buscar la mejor disposición de este elemento con respecto al espacio y a la fuente de iluminación natural. Si eres de ver la tele o de leer en la cama, ya sabes qué hacer: <strong>orienta la cama hasta tener una disposición acorde a tus hábitos</strong>.</p>


