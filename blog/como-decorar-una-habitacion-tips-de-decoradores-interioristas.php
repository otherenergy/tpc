					<h2 style="padding-top:8px;">Iluminación: mucho más que luz para decorar tu habitación</h2>

					<p>Uno de los elementos más importantes a la hora de plantearse cómo decorar una habitación es la iluminación ya que puede afectar a la manera en la que se ven el resto de cosas, desde el color de las paredes hasta la cama y las cortinas.</p>

					<p>Lo ideal sería que el espacio cuente con abundante luz natural, si no es así, vamos a darte una serie de consejos para sacar el mayor partido a la iluminación de tus habitaciones.</p>

					<h3>Tip de interiorista: lámpara a medida para la mesita de noche</h3>

					<p>Además de la fuente de iluminación principal, como puede ser en el techo de la habitación, recomendamos que si en el dormitorio se va a colocar una cama de matrimonio, sobre las dos mesitas de noche se apoyen <strong>dos lámparas a medida</strong>. Así ambos lados quedarán bien iluminados.</p>

					<p>Otra idea más, va. Una de las tendencias que está revolucionando el sector de la decoración de interiores es el uso de una tira de luces led sobre la cama. Esta opción da la libertad a los usuarios de jugar con los diversos colores de las luces, aportando intimidad y personalidad al dormitorio. </p>

					<h3>Truco infalible de un decorador: ¡cortinas al lado!</h3>

					<p>Para sacarle mayor partido a la <strong>luz natural que entra en tu habitación</strong>, recomendamos que se coloquen las cortinas a los lados de los marcos de las ventanas. Además de maximizar la luz que entra, conseguirás crear la ilusión óptica de tener paredes altas y ventanas más grandes, lo que resulta impagable cuando ves la habitación desde fuera y ya no decimos, cuando estás dentro de la misma.</p>

