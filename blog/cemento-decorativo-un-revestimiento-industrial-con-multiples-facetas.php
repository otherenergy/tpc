					<p>Los colores <strong>neutros y crema</strong>, además de ser atemporales, combinan con cualquier estilo decorativo y son armónicos por reflejar luz, calma y equilibrio. Se sigue una tendencia naturista que busca, además, la utilización de texturas y formas que nos recuerden a la naturaleza.</p>

					<p>Y si hay algo que funciona en el cemento decorativo es precisamente el don que tiene para <strong>adaptarse a todo tipo de situaciones y aportar una gran variedad de colores</strong> que, combinados entre sí, puedes dar lugar a combinaciones de lo más interesantes.</p>

