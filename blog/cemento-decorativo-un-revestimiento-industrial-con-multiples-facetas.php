					<p>Aunque puede ser que la palabra "cemento" no resulte atractiva por sí sola, ¿qué te transmite al mencionar cemento decorativo? Si estás pensando en esas superficies de color gris tan características de los materiales robustos y cementicios, te daremos una pista: frío, frío...</p>

					<p>Robusto sí es y duro, como el que más, pero esa idea de concebir el cemento decorativo como algo frío, monótono y aburrido, es algo que quedó temporadas atrás. Ahora, <strong>los materiales industriales son lo más</strong>. Y es, además de por las <strong>prestaciones</strong> que nos han aportado desde sus inicios, por una <strong>estética</strong> que no nos deja retirar de ellas nuestra mirada.</p>

					<p>El cemento decorativo fue un material propio de la arquitectura del New York de los años 50. El mejor aliado para revestir suelos que necesitaban un extra de protección, resistencia y durabilidad, como los pavimentos de comercios y oficinas. Un <strong>estilo inconfundible</strong>, sobre todo, al ser combinado con paredes de ladrillo y complementos en metal o cuero. La estética industrial por excelencia.</p>

