					<h3 class="verde">Lámparas delgadas</h3>
					<p> Puestos a iluminar un salón de dimensiones reducidas, usa lámparas verticales de pie que atraigan las miradas hacia el techo. Deja respirar a las mesas auxiliares y no las llenes de lámparas de mesa. </p>
					<h3 class="verde">Pantallas para la tele, no para la lámpara</h3>
					<p> La lámpara para conseguir el éxito decorativo de tu salón pequeño es muy sencilla: lámpara de una pata que sea delgada, sin pantalla o con la pantalla pequeña. Así de simple. Pura sofisticación. </p>
					<h2>3 estilos clave para la decoración de un salón pequeño </h2>
					<p> Para gustos, estilos. Por eso, y como el tamaño no está negado con el ambiente que quieras crear, te proponemos tres tendencias decorativas que te van a ayudar a sacarle el máximo partido a tu salón pequeño. </p>
					<h3 class="verde">Decoración de un salón pequeño rústico y acogedor</h3>
					<p> Este estilo ayuda a potenciar a adjetivos como "recogidito" o acogedor, que suelen acompañar a este tipo de salones. Tonos rojizos que tiendan al cereza o el burdeos combinados con blancos rotos, son la opción. Y, ya que estamos, con muebles "color madera de verdad" y alguna planta, y no te querrás levantar de tu sillón jamás. </p>
					<h3 class="verde">Salón pequeño pero moderno: ¡sí se puede!</h3>
					<p> Toda la modernidad de la historia cabe en tu pequeño salón, no te preocupes. Un buen equipo para crear un ambiente moderno y lleno de frescura es combinar el color blanco con los detalles caoba claro. Con una mesa blanca, sus correspondientes sillas blancas con patas de madera, una estantería con baldas de madera color blanco y un sofá, también, blanco o claro y tienes un súper salón moderno. </p>
