					<p> <strong>Si no tienes espacio por abajo, búscalo por arriba.</strong> Cambiemos librerías por estanterías. Busquemos soluciones más ágiles y compactas. Por ejemplo, puedes colocar una encima del sofá. Además, de todos modos ya no son tiempos de grandes bibliotecas, a no ser que tengas un castillo. Que parece que no es el caso. </p>
