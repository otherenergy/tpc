					<h2>Consejos útiles para la decoración de un salón pequeño</h2>
					<p> En tu pequeño salón, cada centímetro importa. Por eso, elegir el sofá, las sillas o las mesas es una misión muy importante cuando el espacio es reducido y puede costar más de lo normal. No te preocupes porque a continuación te dejamos <strong>unos trucos de estilismo</strong> para que tu pequeño salón luzca a lo grande. </p>
					<h3 class="verde"> Brazos y respaldos que estén acorde a tu salón </h3>
					<p> Grueso, alto, grande son adjetivos que debes evitar para acertar de pleno al elegir el sofá ideal para tu pequeño salón. De hecho, los brazos y los respaldos han de ser todo lo contrario. Así, te recomendamos cambiar esas palabras por la ligereza. </p>
					<h3 class="verde">Ese sofá, ¡levántalo un poco!</h3>
					<p> Si quieres triunfar con tu sofá procura de que no toque el suelo. Esto lo logrará con unas patas delgadas que permiten que corra el aire por debajo y dan un aspecto más estilizado al saloncito en general. Si necesitas levantar los pies, te recomendamos un puf que vaya a juego con el sofá. </p>
					<h3 class="verde">Sofás-camas, siempre convertibles</h3>
					<p> En un salón con el espacio justito, adaptarse es triunfar. Por eso, un sofá flexible que pueda adoptar diferentes formas según el espacio y tus necesidades vitales es una gran opción. De hecho, <strong>un sofá cama cumple con tu saloncito</strong> y con tus invitados a la perfección ya que te permite optimizar el espacio al máximo. </p>
