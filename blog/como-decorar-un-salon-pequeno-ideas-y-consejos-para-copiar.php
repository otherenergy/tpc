					<p> Esto parece obvio pero es muy importante. Si tienes un salón pequeño, los muebles y objetos decorativos que utilices han de ser coherentes con el espacio en el que están, o lo que es lo mismo, que sean pequeños. Los pequeños muebles auxiliares pueden ser clave. </p>
