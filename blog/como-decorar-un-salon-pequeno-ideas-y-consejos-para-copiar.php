					<h3 class="verde">Piensa en la opción de comprar puffs</h3>
					<p> <strong>Las pufs rígidos son un gran comodín para pequeños hogares.</strong> Ya que sirven para almacenar, como asiento, como cama, como sofá o como mesa. Eso sí, como antes comentábamos el modelo ideal para tu salón será una opción que se separe del suelo. Además, los hay de muchas texturas y colores. Tantas como para adaptarse a tu salón a la perfección. </p>
					<h3 class="verde">Mesas bajitas en salones pequeños</h3>
					<p> Si tu salón no es muy grande, usa una mesa bajita.Y si puede ser (y te gusta) que sea redonda. También tienes otra opción bastante buena que es cambiar esa típica mesa baja de delante del sofá por un nido de dos mesas laterales que aumentarán la sensación de fluidez. Recuerda que los muebles de poca altura son más favorecedores. </p>
					<h3 class="verde">Salones pequeños, alfombras grandes</h3>
					<p> Las cosas no son lo que parecen. A la hora de decorar nuestros salón pequeño, la alfombra que elijamos no tiene por qué ser pequeña. <strong>Poner una alfombra grande que sobrepase los límites del sofá</strong> favorece la sensación de amplitud ya que al mirar el borde verás que coincide con el borde del salón. </p>
					<h3 class="verde">El arte en las paredes va arriba</h3>
					<p> Para elevar el estilo de nuestro pequeño salón conviene tener algunas obras de arte, como cuadros, pequeñas esculturas o jarrones. Si encima lo sitúas en puntos altos de la pared, donde para verlos tengas que levantar la vista, además de más bonito, la estancia te parecerá más grande. </p>
					<h3 class="verde">Piezas verticales... y grandes</h3>
					<p> Las obras de arte ideales para decorar tu salón pequeño, son grandes. Hazte con una o dos piezas para las paredes principales. Más valen dos piezas grandes que 20 pequeñas. </p>
