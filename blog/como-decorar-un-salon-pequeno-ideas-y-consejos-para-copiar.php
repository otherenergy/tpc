					<p> El techo también es una pieza fundamental para ganar espacio en un salón de reducidas dimensiones. <strong>Contrasta los colores de la "quinta pared" con el resto de paredes</strong> puede ayudarte a tener una saloncito de primera. Te damos una combinación: gris oscuro para paredes y blanco brillante para el techo queda fetén. </p>
