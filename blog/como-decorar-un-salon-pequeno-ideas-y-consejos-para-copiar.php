					<h3 class="verde">Salones pequeños alargados... y bonitos</h3>
					<p> Dicen que los salones con forma de "L" son perfectos porque se separan las zonas de forma automática. Bien, aunque tu salón sea pequeño y tenga forma de "L", no pasa nada, con la decoración exacta va a pasar de ser una letra a convertirse en todo un "10". </p>
					<p> Con una mesa extensible con un bonito mantel, para poder aceptar a más invitados en caso que sea necesario, un sofá largo y poco profundo y usar espejos en las paredes enfrentadas y colores oscuros en las laterales que son más largas, te quedará un salón de portada de revista. </p>
					<h3 class="verde">Salones pequeños rectangulares: la forma no importa</h3>
					<p> No importa la forma que tenga tu salón porque sea la que sea, con los elementos decorativos adecuados, puede quedar de maravilla. </p>
					<p> Para empezar, los muebles a la pared. Esto te hará ganar espacio con un simple gesto y, de paso, ampliarás la zona de paso. ¿Estanterías grandes? Claro, pero en vertical. ¿Alfombras? Muy interesantes porque te permiten delimitar zonas y eso es un gran descanso visual. Y, por último, utilizando colores como el blanco, el crema o el gris, crearás un espacio bonito que no parecerá tan pequeño. </p>
