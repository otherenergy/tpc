					<p>El microcemento es un material de construcción compuesto por cemento, resinas y pigmentos. Se aplica en capas muy finas sobre diferentes superficies, como cemento, cerámica, metal, madera, yeso y otros materiales, creando una superficie sin juntas y altamente resistente. Esta capacidad de adherencia lo hace ideal para la renovación de espacios sin la necesidad de retirar los materiales existentes.</p>

